<script>
	 $(document).on('click', '#loginForm', function() {
        var email = $("#email").val();
        var password = $("#password").val();
        if(email == ''){
            $('#emailError').text("Email Required");
        }
        else{
            $('#emailError').text("");
        }
        if(password == ''){
        	$('#passwordError').text("Password Required");
           
        }else{
        	$('#passwordError').text("");
        	
        }
       	var sendInfo = {
         email: email,
         password: password
    	};
        $.ajax({
            url: "{{ url('https://symfony-skeleton.q-tests.com/api/v2/token') }}",
            method:"POST",
            data: JSON.stringify(sendInfo),
            dataType: "json",
            contentType:"application/json",
            success: function(data) {
               localStorage.setItem("access_token",data.token_key);
               localStorage.setItem("refresh_token",data.refresh_token_key);
               location.href = 'dashboard';            
            },
            error: function (request, status, error) {
          
                alert("login and password Not match")
            }
        });
    });
</script>
